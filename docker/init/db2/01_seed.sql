-- FoxSchema demo seed — DB2 LUW
-- Two schemas in foxdb: DEMO_A (source, newer) vs DEMO_B (target, older)
-- Scopes covered: Tables, Views, Indexes (functions/procedures/triggers: TODO)
--
-- Run via the DB2 community image's /var/custom auto-init hook (see
-- docker/init/db2/01_seed.sh) — NOT auto-run by docker-entrypoint-initdb.d
-- like Postgres/MySQL; DB2's image has its own convention.
--
-- CLP terminator note: the default statement terminator is `;`, which breaks
-- compound SQL PL blocks (BEGIN ... END) containing their own `;`-terminated
-- inner statements — the CLP would split on every inner `;`, not just the
-- block's closing one. `--#SET TERMINATOR @` switches the terminator for the
-- block only; switch back to `;` immediately after for the plain statements
-- that follow. Same class of gotcha as MySQL's DELIMITER $$.

-- ============================================================
-- SCHEMA A (source — complete, newer version)
-- ============================================================

-- Drop tables if they exist, to allow clean reseeding.
--#SET TERMINATOR @
BEGIN
  DECLARE CONTINUE HANDLER FOR SQLSTATE '42704' BEGIN END;
  EXECUTE IMMEDIATE 'DROP TABLE DEMO_A.ORDER_ITEMS';
  EXECUTE IMMEDIATE 'DROP TABLE DEMO_A.ORDERS';
  EXECUTE IMMEDIATE 'DROP TABLE DEMO_A.PRODUCTS';
  EXECUTE IMMEDIATE 'DROP TABLE DEMO_A.EMPLOYEE_ROLES';
  EXECUTE IMMEDIATE 'DROP TABLE DEMO_A.CUSTOMERS';
  EXECUTE IMMEDIATE 'DROP TABLE DEMO_A.CATEGORIES';
  EXECUTE IMMEDIATE 'DROP TABLE DEMO_A.INVENTORY';
