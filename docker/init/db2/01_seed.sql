CREATE INDEX DEMO_B.IDX_ORDERS_CUST ON DEMO_B.ORDERS(CUSTOMER_ID);

-- Case 5: composite index column order flipped (IP_ADDRESS first)
CREATE INDEX DEMO_B.IDX_PERF_COMPOSITE ON DEMO_B.WEB_LOGS(IP_ADDRESS, STATUS_CODE);

-- Orphan view (present in target, absent in source)
CREATE VIEW DEMO_B.V_ORDER_SUMMARY AS
SELECT o.id, o.total, o.status, o.created_at,
       oi.qty, oi.unit_price
FROM   DEMO_B.ORDERS o
JOIN   DEMO_B.ORDER_ITEMS oi ON oi.order_id = o.id;
