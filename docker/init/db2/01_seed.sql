CREATE TABLE DEMO_B.CUSTOMERS (
    ID    INT GENERATED ALWAYS AS IDENTITY (START WITH 1 INCREMENT BY 1) PRIMARY KEY,
    NAME  VARCHAR(100) NOT NULL,        -- shorter length (was 150)
    EMAIL VARCHAR(255) NOT NULL UNIQUE
    -- missing: phone, tier, created_at
);

CREATE TABLE DEMO_B.PRODUCTS (
    ID    INT GENERATED ALWAYS AS IDENTITY (START WITH 1 INCREMENT BY 1) PRIMARY KEY,
    NAME  VARCHAR(200) NOT NULL,
    -- PRICE kept as DECIMAL(10,2) to match demo_a: a DB2 column TYPE change
    -- (SET DATA TYPE) puts the table in reorg-pending (SQL0668N rc=7), which
    -- blocks the rest of the same-transaction ALTERs. Type-change coverage
    -- lives in the other 5 dialects' seeds; DB2 exercises column ADD / FK /
    -- index / generated-column paths instead.
    PRICE DECIMAL(10,2) NOT NULL,
    STOCK INT NOT NULL DEFAULT 0
    -- missing: sku, category_id, active, created_at
);

CREATE TABLE DEMO_B.ORDERS (
    ID          INT GENERATED ALWAYS AS IDENTITY (START WITH 1 INCREMENT BY 1) PRIMARY KEY,
    CUSTOMER_ID INT NOT NULL,
    TOTAL       DECIMAL(12,2) NOT NULL,
    STATUS      VARCHAR(20) NOT NULL DEFAULT 'pending',
    CREATED_AT  TIMESTAMP NOT NULL DEFAULT CURRENT TIMESTAMP
    -- missing: notes
);

CREATE TABLE DEMO_B.ORDER_ITEMS (
    ID          INT GENERATED ALWAYS AS IDENTITY (START WITH 1 INCREMENT BY 1) PRIMARY KEY,
    ORDER_ID    INT NOT NULL,
    PRODUCT_ID  INT NOT NULL,
    QTY         INT NOT NULL DEFAULT 1,
    UNIT_PRICE  DECIMAL(10,2) NOT NULL
    -- missing: FK constraints
);

-- INVENTORY: QUANTITY kept as BIGINT to match demo_a (avoid the reorg-pending
-- type change, same as PRODUCTS above). Still exercises ADD COLUMN (last_counted).
CREATE TABLE DEMO_B.INVENTORY (
    ID           INT GENERATED ALWAYS AS IDENTITY (START WITH 1 INCREMENT BY 1) PRIMARY KEY,
    WAREHOUSE    VARCHAR(50) NOT NULL,
    QUANTITY     BIGINT NOT NULL DEFAULT 0,
    BIN_LOCATION VARCHAR(20)
    -- missing column: last_counted
);

-- EMPLOYEE_ROLES: same shape and same FK name as demo_a → no diff.
-- (A constraint-NAME-only rename is inherently a DROP+ADD; non-destructive mode
-- keeps the old FK, so adding a same-columns FK under a new name is a duplicate
-- constraint that DB2 rejects (SQL0541W). That scenario belongs in a
-- destructive-mode test, not the non-destructive E2E flow.)
CREATE TABLE DEMO_B.EMPLOYEE_ROLES (
    EMP_ID  INT NOT NULL,
    ROLE_ID INT NOT NULL,
    PRIMARY KEY (EMP_ID, ROLE_ID),
    CONSTRAINT FK_ACTIVE_EMP_ID FOREIGN KEY (EMP_ID) REFERENCES DEMO_B.CUSTOMERS(ID)
);

-- Case 3: missing generated/derived column
CREATE TABLE DEMO_B.COUPONS (
    ID           INT GENERATED ALWAYS AS IDENTITY (START WITH 1 INCREMENT BY 1) PRIMARY KEY,
    CODE         VARCHAR(50) NOT NULL,
    DISCOUNT     DECIMAL(5,2) NOT NULL
