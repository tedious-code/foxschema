  EXECUTE IMMEDIATE 'DROP TABLE DEMO_A.INTERNATIONAL_SETTINGS';
  EXECUTE IMMEDIATE 'DROP TABLE DEMO_A.WEB_LOGS';
  EXECUTE IMMEDIATE 'DROP VIEW DEMO_A.V_CUSTOMER_ORDERS';
  EXECUTE IMMEDIATE 'DROP VIEW DEMO_A.V_LOW_STOCK';
END@
--#SET TERMINATOR ;

CREATE TABLE DEMO_A.CATEGORIES (
    ID          INT GENERATED ALWAYS AS IDENTITY (START WITH 1 INCREMENT BY 1) PRIMARY KEY,
    NAME        VARCHAR(100) NOT NULL,
    SLUG        VARCHAR(100) NOT NULL UNIQUE,
    PARENT_ID   INT
);

CREATE TABLE DEMO_A.CUSTOMERS (
    ID          INT GENERATED ALWAYS AS IDENTITY (START WITH 1 INCREMENT BY 1) PRIMARY KEY,
    NAME        VARCHAR(150) NOT NULL,
    EMAIL       VARCHAR(255) NOT NULL UNIQUE,
    PHONE       VARCHAR(20),
    TIER        VARCHAR(10) NOT NULL DEFAULT 'standard',
    CREATED_AT  TIMESTAMP NOT NULL DEFAULT CURRENT TIMESTAMP
);

CREATE TABLE DEMO_A.PRODUCTS (
    ID          INT GENERATED ALWAYS AS IDENTITY (START WITH 1 INCREMENT BY 1) PRIMARY KEY,
    NAME        VARCHAR(200) NOT NULL,
    SKU         VARCHAR(50)  NOT NULL UNIQUE,
    PRICE       DECIMAL(10,2) NOT NULL,
    STOCK       INT NOT NULL DEFAULT 0,
    CATEGORY_ID INT,
    ACTIVE      SMALLINT NOT NULL DEFAULT 1,
    CREATED_AT  TIMESTAMP NOT NULL DEFAULT CURRENT TIMESTAMP,
    CONSTRAINT FK_PRODUCT_CATEGORY FOREIGN KEY (CATEGORY_ID) REFERENCES DEMO_A.CATEGORIES(ID)
);

CREATE TABLE DEMO_A.ORDERS (
    ID          INT GENERATED ALWAYS AS IDENTITY (START WITH 1 INCREMENT BY 1) PRIMARY KEY,
    CUSTOMER_ID INT NOT NULL,
    TOTAL       DECIMAL(12,2) NOT NULL,
    STATUS      VARCHAR(20) NOT NULL DEFAULT 'pending',
    NOTES       CLOB,
    CREATED_AT  TIMESTAMP NOT NULL DEFAULT CURRENT TIMESTAMP,
    CONSTRAINT FK_ORDER_CUSTOMER FOREIGN KEY (CUSTOMER_ID) REFERENCES DEMO_A.CUSTOMERS(ID)
);

CREATE TABLE DEMO_A.ORDER_ITEMS (
    ID          INT GENERATED ALWAYS AS IDENTITY (START WITH 1 INCREMENT BY 1) PRIMARY KEY,
    ORDER_ID    INT NOT NULL,
    PRODUCT_ID  INT NOT NULL,
    QTY         INT NOT NULL DEFAULT 1,
    UNIT_PRICE  DECIMAL(10,2) NOT NULL,
    CONSTRAINT FK_ITEM_ORDER   FOREIGN KEY (ORDER_ID)   REFERENCES DEMO_A.ORDERS(ID),
    CONSTRAINT FK_ITEM_PRODUCT FOREIGN KEY (PRODUCT_ID) REFERENCES DEMO_A.PRODUCTS(ID)
);

-- Case 1: Column reordering & column type mutation
CREATE TABLE DEMO_A.INVENTORY (
    ID           INT GENERATED ALWAYS AS IDENTITY (START WITH 1 INCREMENT BY 1) PRIMARY KEY,
    WAREHOUSE    VARCHAR(50) NOT NULL,
    BIN_LOCATION VARCHAR(20),
    QUANTITY     BIGINT NOT NULL DEFAULT 0,
    LAST_COUNTED TIMESTAMP
);

-- Case 2: Constraint name mismatch
CREATE TABLE DEMO_A.EMPLOYEE_ROLES (
    EMP_ID  INT NOT NULL,
    ROLE_ID INT NOT NULL,
    PRIMARY KEY (EMP_ID, ROLE_ID),
    CONSTRAINT FK_ACTIVE_EMP_ID FOREIGN KEY (EMP_ID) REFERENCES DEMO_A.CUSTOMERS(ID)
);

-- Case 3: Expression defaults & generated columns (DB2 uses GENERATED ALWAYS AS)
CREATE TABLE DEMO_A.COUPONS (
    ID           INT GENERATED ALWAYS AS IDENTITY (START WITH 1 INCREMENT BY 1) PRIMARY KEY,
    CODE         VARCHAR(50) NOT NULL,
    DISCOUNT     DECIMAL(5,2) NOT NULL,
    DISPLAY_NAME VARCHAR(100) GENERATED ALWAYS AS (CODE || ' (-' || CHAR(DISCOUNT) || '%)')
);

-- Case 4: Collations & column comments sync
CREATE TABLE DEMO_A.INTERNATIONAL_SETTINGS (
    ID           INT PRIMARY KEY NOT NULL,
    SETTING_KEY  VARCHAR(50) NOT NULL,
    SETTING_VAL  VARCHAR(1000)
);
COMMENT ON COLUMN DEMO_A.INTERNATIONAL_SETTINGS.SETTING_VAL IS 'JSON string payload configuration';

-- Case 5: Composite index order variation
CREATE TABLE DEMO_A.WEB_LOGS (
    ID          BIGINT GENERATED ALWAYS AS IDENTITY (START WITH 1 INCREMENT BY 1) PRIMARY KEY,
    IP_ADDRESS  VARCHAR(45) NOT NULL,
    STATUS_CODE INT NOT NULL,
    LOGGED_AT   TIMESTAMP DEFAULT CURRENT TIMESTAMP
);

-- Performance core indices
CREATE INDEX DEMO_A.IDX_PRODUCTS_CAT ON DEMO_A.PRODUCTS(CATEGORY_ID);
CREATE INDEX DEMO_A.IDX_PRODUCTS_SKU ON DEMO_A.PRODUCTS(SKU);
CREATE INDEX DEMO_A.IDX_ORDERS_CUST  ON DEMO_A.ORDERS(CUSTOMER_ID);
CREATE INDEX DEMO_A.IDX_ORDERS_STAT  ON DEMO_A.ORDERS(STATUS);
CREATE INDEX DEMO_A.IDX_ITEMS_ORD    ON DEMO_A.ORDER_ITEMS(ORDER_ID);

-- Case 5 specific index configuration (STATUS_CODE first)
CREATE INDEX DEMO_A.IDX_PERF_COMPOSITE ON DEMO_A.WEB_LOGS(STATUS_CODE, IP_ADDRESS);

-- Views
CREATE VIEW DEMO_A.V_CUSTOMER_ORDERS AS
SELECT c.id AS customer_id, c.name, c.email, c.tier,
       COUNT(o.id)              AS order_count,
       COALESCE(SUM(o.total),0) AS total_spent
FROM   DEMO_A.CUSTOMERS c
LEFT JOIN DEMO_A.ORDERS o ON o.customer_id = c.id
GROUP BY c.id, c.name, c.email, c.tier;

CREATE VIEW DEMO_A.V_LOW_STOCK AS
SELECT id, name, sku, stock, category_id
FROM   DEMO_A.PRODUCTS
WHERE  stock < 10 AND active = 1;

